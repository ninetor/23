	<h2 class="centered">Как стать участником творческого конкурса?</h2>
	<div class="question-list">
		<div class="question-item">
			<div class="question-link">Для участия в творческом конкурсе «Дарите то, что нравится», необходимо:</div>
			<div class="question-answer">
				<ul>
					<li>
						Написать оригинальное обоснование, почему лучше подарить танк, а не банальный подарок, на ресурсе <a href="http://tanki.mts.by" target="_blank">tanki.mts.by</a>, введя собственный оригинальный текст в специальную форму. Длина текста не должна превышать 90 символов кириллицей с учетом пробелов и знаков препинания.
					</li>
					<li>
						Оформить заявку на участие в конкурсе, указав в специальной форме свой номер телефона, фамилию и имя. Данные будут использоваться для того, чтобы связаться с победителем для последующего вручения приза.
					</li>
					<li>
						Отправить созданную запись при помощи сервиса tanki.mts.by в социальную сеть <a href="https://www.facebook.com/" target="_blank">Facebook</a> или <a href="https://vk.com" target="_blank">ВКонтакте</a>
					</li>
				</ul>
